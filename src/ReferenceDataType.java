public class ReferenceDataType {
    public static void main(String[] args) {
        //Strings
        String name = "Archie";

        //Array
        int[] testScore = {90, 85, 75, 88, 92};

        //String Data
        System.out.println("My name is " + name);

        //Array Data
        System.out.println("The first score listed is " + testScore[0]);
        System.out.println("The second score listed is " + testScore[1]);

        //Changing array value
        testScore[2] = 80;
        System.out.println("The third test score has changed into " + testScore[2]);

        // Example of using object from a class
//        collegeStudents
    }
}
